const express = require('express');
const { ApolloServer } = require('apollo-server-express');
const path = require('path');
const { authMiddleware } = require('./utils/auth');
const { typeDefs, resolvers } = require('./schemas');
const db = require('./config/connection');

const app = express();
const PORT = process.env.PORT || 3001;

const server = new ApolloServer({
  typeDefs,
  resolvers,
  context: authMiddleware, // Middleware for authentication
});

server.applyMiddleware({ app });

app.use(express.urlencoded({ extended: false }));
app.use(express.json());

if (process.env.NODE_ENV === 'production') {
  app.use(express.static(path.join(__dirname, '../client/build'))); // Serve static files in production
}

app.get('*', (req, res) => {
  res.sendFile(path.join(__dirname, '../client/build/index.html')); // Serve the index.html file for all other routes
});

db.once('open', () => {
  app.listen(PORT, () => {
    console.log(`üåç Now listening on localhost:${PORT}`);
    console.log(`Use GraphQL at http://localhost:${PORT}${server.graphqlPath}`); // Log the GraphQL endpoint
  });
});
